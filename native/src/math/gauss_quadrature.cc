index_t Quadrature::tri_num_points(index_t order) {
    switch (order) {
        case 1: return 1;
        case 2: return 3;
        case 3: return 4;
        case 4: return 6;
        case 5: return 7;
        case 6: return 12;
        case 7: return 13;
        default: return 6;
    }
}

void Quadrature::tri_gauss(index_t order, std::vector<std::array<real_t, 2>>& pts,
                           std::vector<real_t>& wts) {
    pts.clear();
    wts.clear();
    
    switch (order) {
        case 1: {
            pts = {{1.0/3.0, 1.0/3.0}};
            wts = {0.5};
            break;
        }
        case 2: {
            real_t pt = 1.0/6.0;
            pts = {{0.5, pt}, {pt, 0.5}, {pt, pt}};
            wts = {1.0/6.0, 1.0/6.0, 1.0/6.0};
            break;
        }
        case 3: {
            real_t a = 0.6;
            real_t b = 0.2;
            pts = {{1.0/3.0, 1.0/3.0},
                   {a, b}, {b, a}, {b, b}};
            wts = {-27.0/96.0 * 0.5,
                   25.0/96.0 * 0.5, 25.0/96.0 * 0.5, 25.0/96.0 * 0.5};
            break;
        }
        case 4:
        case 5: {
            real_t a1 = 0.445948490915965;
            real_t a2 = 0.091576213509771;
            real_t w1 = 0.111690794839005;
            real_t w2 = 0.054975871827661;
            pts = {{a1, a1}, {a1, 1-2*a1}, {1-2*a1, a1},
                   {a2, a2}, {a2, 1-2*a2}, {1-2*a2, a2}};
            wts = {w1, w1, w1, w2, w2, w2};
            break;
        }
        case 6:
        case 7:
        default: {
            real_t a1 = 0.479308067841923;
            real_t a2 = 0.260345966079038;
            real_t a3 = 0.065130102902216;
            real_t b1 = 0.437525248383384;
            real_t w1 = 0.050844906370207;
            real_t w2 = 0.082851075618374;
            real_t w3 = 0.032458497623198;
            pts = {{a1, b1}, {b1, a1}, {b1, 1-a1-b1},
                   {a2, a2}, {a2, 1-2*a2}, {1-2*a2, a2},
                   {a3, a3}, {a3, 1-2*a3}, {1-2*a3, a3},
                   {1.0/3.0, 1.0/3.0}};
            wts = {w1, w1, w1, w2, w2, w2, w3, w3, w3, 0.090817990382754};
            break;
        }
    }
    
    real_t area = 0.5;
    for (auto& w : wts) {
        w *= 2.0 * area;
    }
}

